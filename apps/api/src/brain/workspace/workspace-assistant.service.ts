export type CommandType =
  | 'search'
  | 'report'
  | 'document'
  | 'dashboard'
  | 'task'
  | 'analyze'
  | 'general'

const INTENT_PATTERNS: Array<{ type: CommandType; patterns: RegExp[] }> = [
  { type: 'search',    patterns: [/\b(find|search|show me|list|who|how many|where|look up|get)\b/i] },
  { type: 'report',   patterns: [/\b(report|analyze|analysis|summary|analytics|performance|stats|statistics|kpi|trend)\b/i] },
  { type: 'document', patterns: [/\b(create|write|draft|generate|make)\b.{0,30}\b(document|doc|file|letter|contract|proposal|template)\b/i] },
  { type: 'dashboard',patterns: [/\b(dashboard|widget|chart|graph|visualization|display)\b/i] },
  { type: 'task',     patterns: [/\b(remind|schedule|task|todo|assign|book|create task|set up)\b/i] },
  { type: 'analyze',  patterns: [/\b(analyze|read this|parse|extract|what does this say|interpret)\b/i] },
]

export function detectIntent(message: string): CommandType {
  for (const entry of INTENT_PATTERNS) {
    if (entry.patterns.some(p => p.test(message))) return entry.type
  }
  return 'general'
}

