export async function createOrGetSession(tenantId: string, userId: string, sessionId?: string) {
  if (sessionId) {
    const existing = await prisma.aiWorkspaceSession.findFirst({ where: { id: sessionId, tenantId } })
    if (existing) return existing
  }
  return prisma.aiWorkspaceSession.create({
    data: { tenantId, userId, title: 'New Conversation', messageCount: 0 },
  })
}

export async function listSessions(tenantId: string, userId: string) {
  return prisma.aiWorkspaceSession.findMany({
    where: { tenantId, userId },
    orderBy: { lastActiveAt: 'desc' },
    take: 20,
    include: { _count: { select: { messages: true } } },
  })
}

export async function getSession(sessionId: string, tenantId: string) {
  return prisma.aiWorkspaceSession.findFirst({
    where: { id: sessionId, tenantId },
    include: { messages: { orderBy: { createdAt: 'asc' }, take: 100 } },
  })
}

export async function deleteSession(sessionId: string, tenantId: string) {
  await prisma.aiWorkspaceMessage.deleteMany({ where: { sessionId } })
  await prisma.aiWorkspaceSession.deleteMany({ where: { id: sessionId, tenantId } })
}

export async function chat(params: {
  tenantId: string
  userId: string
  message: string
  sessionId?: string
  provider?: string
}) {
  const { tenantId, userId, message, sessionId, provider = 'mock' } = params

  // 1. Get or create session
  const session = await createOrGetSession(tenantId, userId, sessionId)

  // 2. Save user message
  await prisma.aiWorkspaceMessage.create({
    data: { tenantId, sessionId: session.id, role: 'user', content: message, provider },
  })

  // 3. Detect intent
  const commandType = detectIntent(message)

  // 4. Handle search intent with real data
  let searchResults: Record<string, unknown> | null = null
  if (commandType === 'search') {
    searchResults = await searchAll(tenantId, message)
  }

  // 5. Load memory for context
  const memoryContext = await buildMemoryContext(tenantId, userId)

  // 6. Generate response
  let responseContent: string
  const requiresApproval = ['document', 'task', 'dashboard'].includes(commandType)

  if (provider === 'mock') {
    responseContent = generateRenoBrainResponse(message, commandType)
    if (searchResults && Object.keys(searchResults).length > 0) {
      const resultText = Object.entries(searchResults)
        .map(([module, items]) => `**${module}:** ${(items as any[]).length} result(s)`)
        .join('\n')
      responseContent = `I searched your Reno workspace for "${message.slice(0, 60)}".\n\n${resultText}\n\n${responseContent}`
    }
  } else {
    // When Claude is enabled — use the Reno Brain response with a note
    // In production, this would call callClaudeForChat() with the skill engine
    responseContent = `[Claude AI] ${generateRenoBrainResponse(message, commandType)}`
  }

  // 7. Save assistant message
  const assistantMessage = await prisma.aiWorkspaceMessage.create({
    data: {
      tenantId,
      sessionId: session.id,
      role: 'assistant',
      content: responseContent,
      commandType,
      result: searchResults as any,
      provider,
      requiresApproval,
    },
  })

  // 8. Update session stats
  await prisma.aiWorkspaceSession.update({
    where: { id: session.id },
    data: {
      messageCount: { increment: 2 },
      lastActiveAt: new Date(),
      title: session.messageCount === 0 ? message.slice(0, 100) : undefined,
    },
  })

  // 9. Create command record for actions requiring approval
  if (requiresApproval) {
    await prisma.aiWorkspaceCommand.create({
      data: {
        tenantId, userId,
        sessionId: session.id,
        prompt: message,
        commandType,
        status: 'pending',
        requiresApproval: true,
      },
    })
  }

  // 10. Auto-learn from this conversation
  await autoLearnFromConversation({ tenantId, userId, userMessage: message, commandType })

  return { sessionId: session.id, message: assistantMessage, requiresApproval, commandType }
}

export async function listCommands(tenantId: string, userId?: string, status?: string) {
  const where: any = { tenantId }
  if (userId) where.userId = userId
  if (status) where.status = status
  return prisma.aiWorkspaceCommand.findMany({ where, orderBy: { createdAt: 'desc' }, take: 50 })
}

export async function approveCommand(commandId: string, tenantId: string, userId: string) {
  return prisma.aiWorkspaceCommand.update({
    where: { id: commandId },
    data: { status: 'approved', approvedBy: userId, approvedAt: new Date(), executedAt: new Date() },
  })
}

export async function rejectCommand(commandId: string, tenantId: string) {
  return prisma.aiWorkspaceCommand.update({
    where: { id: commandId },
    data: { status: 'rejected' },
  })
}
